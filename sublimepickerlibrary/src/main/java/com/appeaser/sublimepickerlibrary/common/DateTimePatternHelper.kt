	private val sDateTimePatterns = HashMap<String, Array<String>>()
	private val sLoadedDateTimePatterns = HashMap<String, Array<String>>()

	private fun loadLocaleInternal(locale: Locale): Array<String>? {
		val loadedLocalePattern: Array<String>

		if (!isLocaleLoaded(locale)) {
			loadedLocalePattern = sDateTimePatterns[locale.toString()]
			sLoadedDateTimePatterns[locale.toString()] = loadedLocalePattern
		} else {
			loadedLocalePattern = sLoadedDateTimePatterns[locale.toString()]
		}

		return loadedLocalePattern
	}

	private fun isLocaleLoaded(locale: Locale): Boolean {
		return sLoadedDateTimePatterns.containsKey(locale.toString())
	}

	fun getBestDateTimePattern(givenLocale: Locale, patternIndex: Int): String {
		if (sDateTimePatterns.isEmpty()) {
			initMap()
		}

		val patterns = loadLocaleInternal(givenLocale)

		if (patterns != null && patterns.size > patternIndex) {
			return patterns[patternIndex]
		}

		if (Config.DEBUG) {
			Log.e(TAG, "Could not find date-time pattern for Locale: '"
					+ givenLocale.toString() + "', and map size is: " + sDateTimePatterns.size
					+ ", and loaded map size is: " + sLoadedDateTimePatterns.size)
		}

		return sDefaultDateTimePattern[patternIndex]
	}

